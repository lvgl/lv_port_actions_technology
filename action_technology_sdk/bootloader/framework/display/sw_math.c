void sw_rotate_point32(int32_t *dest_x, int32_t *dest_y,
		int32_t src_x, int32_t src_y, int32_t pivot_x, int32_t pivot_y, uint16_t angle)
{
#ifdef CONFIG_GUI_API_BROM
	p_brom_libgui_api->p_sw_rotate_point32(
			dest_x, dest_y, src_x, src_y, pivot_x, pivot_y, angle);
#else
	int64_t x = src_x - pivot_x;
	int64_t y = src_y - pivot_y;

	assert(angle >= 0 && angle <= 3600);

	*dest_x = (int32_t)((x * sw_cos30(angle) - y * sw_sin30(angle)) >> 30) + pivot_x;
	*dest_y = (int32_t)((x * sw_sin30(angle) + y * sw_cos30(angle)) >> 30) + pivot_y;
#endif /* CONFIG_GUI_API_BROM */
}

void sw_rotate_point16(int16_t *dest_x, int16_t *dest_y,
		int16_t src_x, int16_t src_y, int16_t pivot_x, int16_t pivot_y, uint16_t angle)
{
	int32_t x = src_x - pivot_x;
	int32_t y = src_y - pivot_y;

	assert(angle >= 0 && angle <= 3600);

	*dest_x = round_fixedpoint(x * sw_cos14(angle) - y * sw_sin14(angle), 14) + pivot_x;
	*dest_y = round_fixedpoint(x * sw_sin14(angle) + y * sw_cos14(angle), 14) + pivot_y;
}

void sw_rotate_area16(
		int16_t *dest_x1, int16_t *dest_y1, int16_t *dest_x2, int16_t *dest_y2,
		int16_t src_x1, int16_t src_y1, int16_t src_x2, int16_t src_y2,
		int16_t pivot_x, int16_t pivot_y, uint16_t angle)
{
#ifdef CONFIG_GUI_API_BROM
	p_brom_libgui_api->p_sw_rotate_area16( dest_x1, dest_y1, dest_x2, dest_y2,
			src_x1, src_y1, src_x2, src_y2, pivot_x, pivot_y, angle);
#else
	int32_t pt[4][2] = {
		{ PX_FIXEDPOINT1(src_x1), PX_FIXEDPOINT1(src_y1), },
		{ PX_FIXEDPOINT1(src_x1), PX_FIXEDPOINT1(src_y2), },
		{ PX_FIXEDPOINT1(src_x2), PX_FIXEDPOINT1(src_y2), },
		{ PX_FIXEDPOINT1(src_x2), PX_FIXEDPOINT1(src_y1), },
	};
	int i;

	assert(angle >= 0 && angle <= 3600);

	sw_rotate_point32(&pt[0][0], &pt[0][1], pt[0][0], pt[0][1], FIXEDPOINT1(pivot_x), FIXEDPOINT1(pivot_y), angle);
	*dest_x1 = pt[0][0];
	*dest_y1 = pt[0][1];
	*dest_x2 = pt[0][0];
	*dest_y2 = pt[0][1];

	for (i = 1; i < 4; i++) {
		sw_rotate_point32(&pt[i][0], &pt[i][1], pt[i][0], pt[i][1], FIXEDPOINT1(pivot_x), FIXEDPOINT1(pivot_y), angle);

		if (*dest_x1 > pt[i][0])
			*dest_x1 = pt[i][0];
		else if (*dest_x2 < pt[i][0])
			*dest_x2 = pt[i][0];

		if (*dest_y1 > pt[i][1])
			*dest_y1 = pt[i][1];
		else if (*dest_y2 < pt[i][1])
			*dest_y2 = pt[i][1];
	}

	*dest_x1 = ROUND_FIXEDPOINT1(*dest_x1 - FIXEDPOINT1_0_5);
	*dest_y1 = ROUND_FIXEDPOINT1(*dest_y1 - FIXEDPOINT1_0_5);
	*dest_x2 = ROUND_FIXEDPOINT1(*dest_x2 - FIXEDPOINT1_0_5);
	*dest_y2 = ROUND_FIXEDPOINT1(*dest_y2 - FIXEDPOINT1_0_5);
#endif /* CONFIG_GUI_API_BROM */
}
